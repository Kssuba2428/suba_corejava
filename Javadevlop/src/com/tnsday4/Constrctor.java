package com.tnsday4;

public class Constrctor {
	Constrctor(){
		System.out.println("sample");}
	Constrctor(int a){
		System.out.println("the value is a");}
	Constrctor(float b){
			System.out.println("the value is b");}
	Constrctor(int a,int b){
		 System.out.println("the value is a&b");
		}

}
